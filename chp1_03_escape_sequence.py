print("hello \"world\" world")

'''escape sequences
\'  - single quote
\"  - double quote
\\  - backslash 
\n  - new line 
\t  - tab 
\b  - backspace'''

#print('I'm harshit') will throw an error
print('I\'m Harshit')

print("line A")
print("line B")
#it can also written as 
print("line A\n line B") #space after backslash n 

print("name\tHarshit")

print("this is \\ backslash") #double backslash is single backslash

print("hell\blo")



